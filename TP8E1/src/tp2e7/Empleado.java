package tp2e7;

public class Empleado {
	private String nombre;
	private int documento;
	
	public void setNombre(String name) {
		nombre = name;
	}
	
	public void setDocumento(int doc) {
		documento = doc;
	}
	
	public String getNombre() {
		return nombre;
	}
	
	public int getDocumento() {
		return documento;
	}
}
